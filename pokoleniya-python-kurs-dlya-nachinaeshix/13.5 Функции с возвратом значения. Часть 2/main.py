# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def is_correct_bracket(text):
    if not text:
        return False

    total = 0
    for i in text:
        if i == '(':
            total += 1
        elif i == ')':
            total -= 1
            if total < 0:
                return False

    return total == 0

# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
