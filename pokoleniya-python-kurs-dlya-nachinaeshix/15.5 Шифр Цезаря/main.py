# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –°–∏–º–≤–æ–ª—ã, –Ω–µ —è–≤–ª—è—é—â–∏–µ—Å—è –∞–Ω–≥–ª–∏–π—Å–∫–∏–º–∏ –±—É–∫–≤–∞–º–∏, –Ω–µ –∏–∑–º–µ–Ω—è—é—Ç—Å—è.

import string

# –û—á–∏—â–∞–µ–º —Å–ª–æ–≤–æ –æ—Ç —Å–∏–º–≤–æ–ª–æ–≤
def removed_chars(s):
    del_chars = ',.!#$:()"~@%;?'
    return s.translate(str.maketrans('', '', del_chars))

def encrypt_word(word):
    """
    –§—É–Ω–∫—Ü–∏—è —à–∏—Ñ—Ä—É–µ—Ç —Å–ª–æ–≤–æ —Å –ø–æ–º–æ—â—å—é —à–∏—Ñ—Ä–∞ –¶–µ–∑–∞—Ä—è —Å —Ü–∏–∫–ª–∏—á–µ—Å–∫–∏–º —Å–¥–≤–∏–≥–æ–º –Ω–∞ –¥–ª–∏–Ω—É —Å–ª–æ–≤–∞.
    """
    word_clear = removed_chars(word) # –û—á–∏—â–∞–µ–º —Å–ª–æ–≤–æ –æ—Ç —Å–∏–º–≤–æ–ª–æ–≤
    shift = len(word_clear) # –î–ª–∏–Ω–∞ —Å–ª–æ–≤–∞ –±–µ–∑ —Å–∏–º–≤–æ–ª–æ–≤
    lower_alphabet = string.ascii_lowercase # –ü–æ–ª—É—á–∞–µ–º –∞–ª—Ñ–∞–≤–∏—Ç –≤ –Ω–∏–∂–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ
    upper_alphabet = string.ascii_uppercase # –ü–æ–ª—É—á–∞–µ–º –∞–ª–∞–≤–∏—Ç –≤ –≤–µ—Ä—Ö–Ω–µ–º —Ä–µ–≥–∏—Å—Ç—Ä–µ
    encrypted = "" # –°–æ–∑–¥–∞–µ–º –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫ –≤ –∫–æ—Ç–æ—Ä—ã–π –±—É–¥–µ–º –¥–æ–±–∞–≤–ª—è—Ç—å —à–∏—Ñ—Ä–æ–≤–∞–Ω–Ω—ã–µ –±—É–∫–≤—ã
    for char in word:
        if char in lower_alphabet:
            # –®–∏—Ñ—Ä—É–µ–º —Å—Ç—Ä–æ—á–Ω—ã–µ –±—É–∫–≤—ã
            encrypted += lower_alphabet[(lower_alphabet.index(char) + shift) % 26]
        elif char in upper_alphabet:
            # –®–∏—Ñ—Ä—É–µ–º –ø—Ä–æ–ø–∏—Å–Ω—ã–µ –±—É–∫–≤—ã
            encrypted += upper_alphabet[(upper_alphabet.index(char) + shift) % 26]
        else:
            # –û—Å—Ç–∞–≤–ª—è–µ–º –Ω–µ –±—É–∫–≤–µ–Ω–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã –∫–∞–∫ –µ—Å—Ç—å
            encrypted += char
    return encrypted

def encrypt_sentence(sentence):
    """
    –§—É–Ω–∫—Ü–∏—è —à–∏—Ñ—Ä—É–µ—Ç –≤—Å–µ —Å–ª–æ–≤–∞ –≤ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–∏–∏ —Å –ø–æ–º–æ—â—å—é —à–∏—Ñ—Ä–∞ –¶–µ–∑–∞—Ä—è.
    """
    words = sentence.split()
    encrypted_words = []
    for word in words:
        encrypted_word = encrypt_word(word)
        encrypted_words.append(encrypted_word)
    return " ".join(encrypted_words)


# –í–≤–æ–¥ —Ñ—Ä–∞–∑—ã –∏ –≤—ã–≤–æ–¥ —à–∏—Ñ—Ä–æ–≤–∞–Ω–æ–π
sentence = input().strip()
