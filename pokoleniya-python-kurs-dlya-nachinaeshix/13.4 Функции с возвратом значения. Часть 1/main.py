def merge(list1, list2):
    first, second = list1[:], list2[:]
    result = []

    while first and second:
        if first[0] <= second[0]:
            item = first.pop(0)
        else:
            item = second.pop(0)
        result.append(item)

    result.extend(first if first else second)
    return result


