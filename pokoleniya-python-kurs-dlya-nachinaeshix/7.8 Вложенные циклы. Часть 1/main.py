# –°–∞–º—ã–π –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏–π —Ü–∏–∫–ª —Å–¥–µ–ª–∞–µ—Ç 60 –∏—Ç–µ—Ä–∞—Ü–∏–π –¥–ª—è –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏ —Å—Ä–µ–¥–Ω–µ–≥–æ —Ü–∏–∫–ª–∞. –°—Ä–µ–¥–Ω–∏–π —Ü–∏–∫–ª —Å–¥–µ–ª–∞–µ—Ç 60 –∏—Ç–µ—Ä–∞—Ü–∏–π –¥–ª—è
# –∫–∞–∂–¥–æ–π –∏—Ç–µ—Ä–∞—Ü–∏–∏ —Å–∞–º–æ–≥–æ –≤–Ω–µ—à–Ω–µ–≥–æ —Ü–∏–∫–ª–∞. –ö–æ–≥–¥–∞ —Å–∞–º—ã–π –≤–Ω–µ—à–Ω–∏–π —Ü–∏–∫–ª —Å–¥–µ–ª–∞–µ—Ç 24 –∏—Ç–µ—Ä–∞—Ü–∏–∏,
# —Å—Ä–µ–¥–Ω–∏–π —Å–¥–µ–ª–∞–µ—Ç 24‚ãÖ60=1440 –∏—Ç–µ—Ä–∞—Ü–∏–π, –∏ —Å–∞–º—ã–π –≤–Ω—É—Ç—Ä–µ–Ω–Ω–∏–π —Ü–∏–∫–ª —Å–¥–µ–ª–∞–µ—Ç 24 ‚ãÖ60‚ãÖ60=86400 –∏—Ç–µ—Ä–∞—Ü–∏–π!

