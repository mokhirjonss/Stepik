for i in range(100, 1000):  # –ø–µ—Ä–µ–±–∏—Ä–∞–µ–º —á–∏—Å–ª–∞ –æ—Ç 100 –¥–æ 999
    if i % 10 == 7:         # –∏—Å–ø–æ–ª—å–∑—É–µ–º –æ—Å—Ç–∞—Ç–æ–∫ –æ—Ç –¥–µ–ª–µ–Ω–∏–µ –Ω–∞ 10, –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è –ø–æ—Å–ª–µ–¥–Ω–µ–π —Ü–∏—Ñ—Ä—ã
        print(i)

# –û–±—Ä–∞—Ç–∏—Ç–µ –≤–Ω–∏–º–∞–Ω–∏–µ, –≤ –∫–∞—á–µ—Å—Ç–≤–µ –≤—Ç–æ—Ä–æ–≥–æ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞ –º—ã –ø–µ—Ä–µ–¥–∞–ª–∏ —á–∏—Å–ª–æ 1000.

