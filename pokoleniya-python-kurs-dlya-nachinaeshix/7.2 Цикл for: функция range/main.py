# –ü–µ—Ä–µ–¥–∞–≤–∞—è –¥–≤–∞ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞ –≤ —Ñ—É–Ω–∫—Ü–∏—é range() –º—ã –º–æ–∂–µ–º –≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å –ª—é–±—É—é –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª —Å —à–∞–≥–æ–º 1.
# –ù–æ, —á—Ç–æ –¥–µ–ª–∞—Ç—å –µ—Å–ª–∏ –Ω—É–∂–Ω–æ –ø–æ–º–µ–Ω—è—Ç—å —à–∞–≥? –ö–∞–∫ –±—ã—Ç—å –µ—Å–ª–∏ –º—ã —Ö–æ—Ç–∏–º —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å
# –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —á–∏—Å–µ–ª 5, 10, 15, 20, 25? –í —ç—Ç–æ–º —Å–ª—É—á–∞–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –µ—â–µ –æ–¥–Ω–∞ –ø–µ—Ä–µ–≥—Ä—É–∑–∫–∞ —Ñ—É–Ω–∫—Ü–∏–∏ range(),
# –ø—Ä–∏–Ω–∏–º–∞—é—â–∞—è —Ç—Ä–∏ –ø–∞—Ä–∞–º–µ—Ç—Ä–∞: range(n, m, k). –ü–µ—Ä–≤—ã–π –ø–∞—Ä–∞–º–µ—Ç—Ä –∑–∞–¥–∞–µ—Ç —Å—Ç–∞—Ä—Ç –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏,
# –≤—Ç–æ—Ä–æ–π –ø–∞—Ä–∞–º–µ—Ç—Ä –∑–∞–¥–∞–µ—Ç —Å—Ç–æ–ø –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ –∏ —Ç—Ä–µ—Ç–∏–π ‚Äì —à–∞–≥ –≥–µ–Ω–µ—Ä–∞—Ü–∏–∏ —á–∏—Å–µ–ª.

# –ù–∞–ø—Ä–∏–º–µ—Ä, –≤—ã–∑–æ–≤ —Ñ—É–Ω–∫—Ü–∏–∏ range(1, 10, 2) —Å–æ–∑–¥–∞—Å—Ç –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —á–∏—Å–µ–ª 1, 3, 5, 7, 9,
