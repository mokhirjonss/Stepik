# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ ¬´YES¬ª –µ—Å–ª–∏ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å –µ–≥–æ —Ü–∏—Ñ—Ä –ø—Ä–∏ –ø—Ä–æ—Å–º–æ—Ç—Ä–µ —Å–ø—Ä–∞–≤–∞ –Ω–∞–ª–µ–≤–æ —è–≤–ª—è–µ—Ç—Å—è —É–ø–æ—Ä—è–¥–æ—á–µ–Ω–Ω–æ–π
# –ø–æ –Ω–µ—É–±—ã–≤–∞–Ω–∏—é –∏ ¬´NO¬ª –≤ –ø—Ä–æ—Ç–∏–≤–Ω–æ–º —Å–ª—É—á–∞–µ.

n = int(input())
flag = True
last_digit = n % 10

while n > 0:
    if last_digit > n % 10:
        flag = False

    last_digit = n % 10
    n //= 10

if flag:
    print("YES")
else:
    print("NO")
