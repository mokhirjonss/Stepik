# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞–µ—Ç—Å—è –æ–¥–Ω–æ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ.

# –§–æ—Ä–º–∞—Ç –≤—ã—Ö–æ–¥–Ω—ã—Ö –¥–∞–Ω–Ω—ã—Ö
# –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –≤—ã–≤–µ—Å—Ç–∏ –∑–Ω–∞—á–µ–Ω–∏—è —É–∫–∞–∑–∞–Ω–Ω—ã—Ö –≤–µ–ª–∏—á–∏–Ω –≤ —É–∫–∞–∑–∞–Ω–Ω–æ–º –ø–æ—Ä—è–¥–∫–µ.
number_digits = 0  # –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ü–∏—Ñ—Ä
last_digit = 0  # –ø–æ—Å–ª–µ–¥–Ω—è—è —Ü–∏—Ñ—Ä–∞
even_digits = 0  # –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —á–µ—Ç–Ω—ã—Ö —Ü–∏—Ñ—Ä
total = 0  # —Å—É–º–º—É –µ–≥–æ —Ü–∏—Ñ—Ä, –±–æ–ª—å—à–∏—Ö –ø—è—Ç–∏;
multiply = 1  # –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ —Ü–∏—Ñ—Ä
how_many = 0
number = int(input())
last_digit2 = number % 10
while number != 0:
    last_digit1 = number % 10
    if last_digit2 == last_digit1:
        last_digit += 1
    if last_digit1 == 3:
        number_digits += 1
    if last_digit1 % 2 == 0:
        even_digits += 1
    if last_digit1 > 5:
        total += last_digit1
    if last_digit1 > 7:
        multiply *= last_digit1
    if last_digit1 == 0 or last_digit1 == 5:
        how_many += 1
    number //= 10
print(number_digits, last_digit, even_digits, total, multiply, how_many, sep='\n')
